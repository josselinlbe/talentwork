export async function createEntity(data: {
  name: string;
  slug: string;
  order: number;
  prefix: string;
  title: string;
  titlePlural: string;
  isFeature: boolean;
  isAutogenerated: boolean;
  hasApi: boolean;
  requiresLinkedAccounts: boolean;
  icon: string;
  active: boolean;
}) {
  const entity = await db.entity.create({
    data,
  });

  const webhooks = [
    {
      action: "Created",
      method: "POST",
      endpoint: "",
    },
    {
      action: "Updated",
      method: "POST",
      endpoint: "",
    },
    {
      action: "Deleted",
      method: "POST",
      endpoint: "",
    },
  ];

  await Promise.all(
    webhooks.map(async (webhook) => {
      return await db.entityWebhook.create({
        data: {
          entityId: entity.id,
          action: webhook.action,
          method: webhook.method,
          endpoint: webhook.endpoint,
        },
      });
    })
  );

  await Promise.all(
    defaultProperties.map(async (property) => {
      return await db.property.create({
        data: {
          entityId: entity.id,
          ...property,
        },
      });
    })
  );

  return entity;
}

