export async function getAllRowsUsage(tenantId: string): Promise<RowsUsage[]> {
  const countEntities = await db.row.groupBy({
    by: ["entityId"],
    _count: true,
    where: {
      OR: [
        {
          tenantId,
        },
        {
          linkedAccount: {
            OR: [
              {
                providerTenantId: tenantId,
              },
              {
                clientTenantId: tenantId,
              },
            ],
          },
        },
      ],
    },
  });
  return countEntities;
}

